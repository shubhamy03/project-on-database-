 <h3>Type Of Questions</h3>
 <p>The Common Admission Test (CAT), like virtually all large-scale exams, utilises multiple forms, or versions, of the test. Hence there are two types of scores involved: a raw score and a scaled score.

The raw score is calculated for each section based on the number of questions one answered correctly, incorrectly, or left unattempted. Candidates are given +3 points for each correct answer and -1 point for each incorrect answer. No points are given for questions that are not answered. The raw scores are then adjusted through a process called equating. Equated raw scores are then placed on a common scale or metric to ensure appropriate interpretation of the scores. This process is called scaling.</p>
